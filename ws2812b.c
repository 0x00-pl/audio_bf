void WS2812B_TxRes(void)
{
	WS2812B_PIN_CLR();
	for (uint64_t i = 0; i < 1000; i++)
		asm volatile("nop");
}

void WS2812B_SetLedRGB(uint8_t r, uint8_t g, uint8_t b)
{
	uint64_t frameBuf = (g << 16) | (r << 8) | b;

	for (uint64_t mask = 0x800000; mask > 0; mask >>= 1) {
		if (frameBuf & mask)
			WS2812B_Tx1();
		else
			WS2812B_Tx0();
	}
}
